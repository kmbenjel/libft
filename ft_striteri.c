void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	if (!s || !f)
		return (0);
	i = 0;
	while (*(s + i))
	{
		*(s + i) = f(i, *(s + i));
		i++;
	}
	return (0);
}
